// ============================================================================
// レンダリング同期モード（コンパイル時切り替え）
// ----------------------------------------------------------------------------
// AQ_RENDER_PIPELINED を define すると、レンダースレッドをゲームスレッドより
// 1 フレーム先行させてオーバーラップ実行する（CPU/GPU パイプライン化＝性能重視）。
// undef のままだと、毎フレーム描画完了を待ってから次フレームへ進む直列実行
// （メモリ削減重視）になる。
//
//   直列 (default):
//     - メイン HDR RT     : 1 枚            （MAIN_RT_COUNT = 1）
//     - レンダーリング    : 2 スロット      （FRAMES_IN_FLIGHT = 2）
//     - フレーム同期      : WaitForCompletion()（その場で全完了待ち）
//     - メイン RT トグル  : なし（常に index 0）
//
//   非同期 (AQ_RENDER_PIPELINED):
//     - メイン HDR RT     : 2 枚（ダブルバッファ） （MAIN_RT_COUNT = 2）
//     - レンダーリング    : 4 スロット（=2frame×2submit）（FRAMES_IN_FLIGHT = 4）
//     - フレーム同期      : WaitForPipelinedFrame()（前フレーム完了のみ待つ）
//     - メイン RT トグル  : 毎フレーム（フレーム N と N+1 で別 RT を使う）
//
// 切り替えるにはこの行のコメントを外す / 付け直すだけでよい。
// ----------------------------------------------------------------------------
//#define AQ_RENDER_PIPELINED
// ============================================================================
